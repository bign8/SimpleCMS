	<h4>During the Study</h4>
	<p>Once the Stardust recording unit is turned on, do not turn off the unit until the end of the study when you wake up for the day.</p>
	<p>Press the Patient Event Marker button for the following events:</p>
	<ul>
		<li>Lights Out for the night</li>
		<li>Visiting the restroom</li>
		<li>Getting up for a snack</li>
		<li>Waking up for any reason</li>
		<li>Lights On for the day</li>
	</ul>
