	<h4>Setting Up For The Sleep Study</h4>
	<p>Prepare for the night of your sleep study according to the instructions provided from Sleep Diagnostics, Inc.</p>
	<p>Place the StarDust recording equipment on in the following order:</p>
	<ol>
		<li>Place the cord for the POUCH around the back of your neck so that the POUCH hangs just above your belly button.</li>
		<li>Place the StarDust recorder in the POUCH.</li>
		<li>Undo the clip on the blue RESPIRATORY EFFORT BELT and wrap it around your back and snap the tongue of the clip together. The effort belt is pulled through the loop on the back of the POUCH. Tighten the strap until the belt fits snugly.</li>
		<li>Place the blue nasal/oral tubing nosepiece (nasal cannula) in your nose with the curves of the nasal cannula coming down towards you and wrap the tubing around your ears. Ie: (In the nose and around the ears). The curve of the oral flow will curve into your mouth. (This will seem uncomfortable at first but you will adjust to this sensation). The lead collar will rest underneath your chin. Slide the plastic lead collar slide up until the cannula is snug. Use 2 pieces of tape to tape the cannula to your cheeks on the right and left side. (see picture)</li>
		<li>Put the SpO2 sensor (finger clip) on the finger of your dominant hand. Tape the sensor WIRE in two places.
			<ul>
				<li>Just above the sensor on the finger (see picture)</li>
				<li>Make a fist and bring the WIRE to your wrist and tape it again. Making the fist will leave enough slack in the WIRE so you can move your wrist without pulling the wire to tightly on your finger. (see picture).</li>
			</ul>
			<img src="StarDust5.jpg" width="500" height="337" alt="StarDust Attachment" style="border: solid thin #000;">
		</li>
		<li>Remove the StarDust recorder from the POUCH and turn it on by sliding the button on the side upward until you see the green light turn on.</li>
		<li>Refer to the Troubleshooting guide if an AMBER light flashes.</li>
		<li>You are now ready to begin the sleep study. The placement should match the figure here.</li>
		<li>The effort belt feeds through a loop in the back of the Pouch.</li>
	</ol>
