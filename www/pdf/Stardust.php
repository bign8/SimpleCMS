	<h4>Trouble Shooting</h4>
	<p>If after you turn on the device the LED is flashing AMBER</p>
	<table border="1" cellspadding="3" cellspacing="0" style="border-collapse: collapse;">
		<tbody>
			<tr>
				<th>Problem</th>
				<th>Solution</th>
			</tr>
			<tr>
				<td>One flash indicates an SpO2 (finger clip) lead error</td>
				<td>Reposition finger probe and re-tape</td>
			</tr>
			<tr>
				<td>Two flashes indicates low airflow</td>
				<td>Reposition nasal cannula and re-tape</td>
			</tr>
			<tr>
				<td>Three flashes indicates low effort sensing</td>
				<td>Tighten the black abdominal effort band</td>
			</tr>
			<tr>
				<td>Four flashes indicates full memory</td>
				<td>Call Provider (Sleep Diagnostics, Inc)</td>
			</tr>
			<tr>
				<td>Five flashes indicates that the 9 volt battery is low</td>
				<td>Replace 9 volt battery</td>
			</tr>
			<tr>
				<td>Six flashes indicates a corrupt memory</td>
				<td>Call Provider (Sleep Diagnostics, Inc)</td>
			</tr>
