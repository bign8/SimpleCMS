	<p>
		<span style="font-weight: bold; text-decoration: underline;">What Is A Sleep Study?</span><br />
		A sleep study is an all-night study designed to provide vital information about your sleeping health by monitoring various signals from your body. These signals include: brain-waves, 
		heart activity, muscle activity, eye movements, breathing patterns and oxygen levels.  The signals are obtained from small sensors attached to you scalp, face and body.  No needles are 
		used and you should not experience any unusual discomfort. Sleep studies enable physicians to evaluate conditions which occur only during sleep and are non-detectable during a regular 
		office exam. Sleep studies help to identify causes of various sleep-related health disorders and to plan for effective treatment.
	</p>
	<p>
		<span style="font-weight: bold; text-decoration: underline;">What is the procedure during a sleep study?</span><br />
		It will take a sleep technician about 40 minutes to attach small sensors to your head, chest stomach and legs and you will be asked to fill out short pre-sleep questionnaire. After 
		set up you can unwind as usual (read, or watch TV).  The tech will monitor you from another room. If you need anything just ask aloud in a normal speaking voice and the tech will 
		come into the room to aid you.  You can use the restroom at any time in the night, it is an easy process (urinal or bedside commode is available upon request).  If a breathing problem 
		is observed during your study the tech may awaken you to ask you to try a device that treats breathing problems during sleep.  Generally the tech will discuss this with you prior to bed. 
		The device is PAP (Positive Airway Pressure) and it includes a small mask that fits over your nose or silicone plugs in your nostrils.
	</p>
	<p>
		<span style="font-weight: bold; text-decoration: underline;">What is the procedure after a sleep study?</span><br />
		It will take approximately 7-9 working days for the study results to get back to your primary care physician. He/She will contact you concerning the results. A copy of the interpretation 
		and a sleep center survey will be mailed to you.
	</p><br />
